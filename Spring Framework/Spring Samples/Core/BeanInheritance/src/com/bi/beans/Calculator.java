package com.bi.beans;

public class Calculator {
	private float pi;
	private float alpha;
	private float beta;
	private float gama;

	public float getPi() {
		return pi;
	}

	public void setPi(float pi) {
		this.pi = pi;
	}

	public float getAlpha() {
		return alpha;
	}

	public void setAlpha(float alpha) {
		this.alpha = alpha;
	}

	public float getBeta() {
		return beta;
	}

	public void setBeta(float beta) {
		this.beta = beta;
	}

	public float getGama() {
		return gama;
	}

	public void setGama(float gama) {
		this.gama = gama;
	}

}
