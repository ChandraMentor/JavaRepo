package com.ss.form;

import org.apache.struts.action.ActionForm;

public class StudentForm extends ActionForm {
	private String id;
	private String name;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
