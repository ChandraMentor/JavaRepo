class StatDemo {
	//static variables(class variables)
	static int a,b,c;
	//static methods
	static void setData(int x,int y) {
		a=x;
		b=y;
	}
	//instance method
	void getData() {
		c=a+b;
		System.out.println("The sum is" + c);
	}
	public static void main(String args[]) {
		StatDemo.setData(40,50);
		StatDemo sd = new StatDemo();
		sd.getData();
	}
}

